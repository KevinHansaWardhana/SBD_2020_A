    private void tablekamarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablekamarMouseClicked
        // TODO add your handling code here:
        int baris=tablekamar.rowAtPoint(evt.getPoint());
        String idkamar=tablekamar.getValueAt(baris, 0).toString();
        Id_kamar.setText(idkamar);
        
        String no_kamar=tablekamar.getValueAt(baris, 1).toString();
        nokamar.setText(no_kamar);
        
        String Harga=tablekamar.getValueAt(baris, 2).toString();
        harga.setText(Harga);   
        
        String Jeniskamar=tablekamar.getValueAt(baris, 3).toString();
        jeniskamar.setText(Jeniskamar);
    
        String Status=tablekamar.getValueAt(baris, 4).toString();
        status.setText(Status);
    
    }//GEN-LAST:event_tablekamarMouseClicked

