    private void tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahActionPerformed
        // TODO add your handling code here:
        String idkamar=Id_kamar.getText();
        String no_kamar=nokamar.getText();
        String Harga=harga.getText();
        String jenis_kamar=jeniskamar.getText();
        String Status=status.getText();

         try{
             Statement statement= (Statement) config.GetConnection().createStatement();
             statement.executeUpdate("insert into kamar VALUES('"+idkamar+
                     "','"+no_kamar+
                     "','"+Harga+
                     "','"+jenis_kamar+
                     "','"+Status+"');");
             statement.close();
             JOptionPane.showMessageDialog(null,"Data berhasil disimpan");
         }catch(Exception t){
             JOptionPane.showMessageDialog(null,"Data gagal disimpan");  
         }   
         tampilkan_data();
         kosongkan_form();

    }//GEN-LAST:event_tambahActionPerformed

